import mongoose from 'mongoose';

const packageAddonSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  isActive: {
    type: Boolean,
    default: true,
  },
}, {
  timestamps: true,
});

const PackageAddon = mongoose.model('PackageAddon', packageAddonSchema);

export default PackageAddon;
